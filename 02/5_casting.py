# to int
a = int(10.5)
b = int('66')
c = int(True)
d = int(False)
print('to int results:', a, b, c, d)


# to float
e = float(19)
f = float('22')
g = float('66.6')
h = float(True)
i = float(False)
print('to float results:', e, f, g, h, i)


# to string
j = str(19)
k = str(66.6)
l = str(4-3j)
m = str(True)
n = str(False)
o = str(None)
print('to string results:', j, k, l, m, n, o)


# too boolean
p = bool(12)
