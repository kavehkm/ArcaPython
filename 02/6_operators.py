# arithmetic
number1 = 100
number2 = 50

result1 = number1 + number2
result2 = number1 - number2
result3 = number1 * number2
result4 = number1 / number2
result5 = number1 // number2
result6 = number1 % number2
result7 = number1 ** number2

print(result1, result2, result3, result4, result5, result6, result7)


# assignment(compound)
number3 = 100
print(number3)
number3 += 2            # number3 = number3 + 2
print(number3)
number3 -= 3            # number3 = number3 - 3
print(number3)
number3 *= 4            # number3 = number3 * 4
print(number3)
number3 /= 5            # number3 = number3 / 5
print(number3)
number3 //= 6           # number3 = number3 // 6
print(number3)
number3 %= 7            # number3 = number3 % 7
print(number3)
number3 **= 8           # number3 = number3 ** 8
print(number3)
